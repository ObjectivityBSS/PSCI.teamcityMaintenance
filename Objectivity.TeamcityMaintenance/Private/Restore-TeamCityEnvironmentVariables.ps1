function Restore-TeamCityEnvironmentVariables {
    <#
    .SYNOPSIS
    Restores TeamCity environment variables from backup.

    .PARAMETER TeamcityBackupPaths
    Object containing information about backup paths - generated by Get-TeamcityBackupPaths.

    .EXAMPLE
    Restore-TeamCityEnvironmentVariables -TeamcityBackupPaths $TeamcityBackupPaths
    #>

    [CmdletBinding()]
    [OutputType([void])]
    param(       
        [Parameter(Mandatory=$true)]
        [PSCustomObject] 
        $TeamcityBackupPaths
    )

    $backupDir = $TeamcityBackupPaths.EnvDir
    $envVarsFile = Join-Path -Path $backupDir -ChildPath "envVars.txt"
    if (!(Test-Path -LiteralPath $envVarsFile)) {
        Write-Log -Warn "No TEAMCITY environment variables file at '$envVarsFile'. You will need to set TEAMCITY_SERVER_MEM_OPTS manually (according to TeamCity documentation)."
        Request-UserInputToContinue
        return
    }
    $envVars = ConvertFrom-Json -InputObject (Get-Content -Path $envVarsFile -ReadCount 0 | Out-String)
    foreach ($envVar in $envVars) {
        Write-Log -Info "Setting environment variable '$($envVar.Key)' to '$($envVar.Value)'" -Emphasize
        [Environment]::SetEnvironmentVariable($envVar.Key, $envVar.Value, "Machine")
    }
    Update-EnvironmentVariables
}